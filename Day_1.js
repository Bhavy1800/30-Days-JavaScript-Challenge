var numberVar = 10;
console.log(numberVar); // Output: 10

let stringLet = "Hello, World!";
console.log(stringLet); // Output: Hello, World!

const booleanConst = true;
console.log(booleanConst); // Output: true

let num = 42;
let str = "JavaScript";
let bool = false;
let obj = { name: "John", age: 30 };
let arr = [1, 2, 3];

console.log(typeof num); // Output: number
console.log(typeof str); // Output: string
console.log(typeof bool); // Output: boolean
console.log(typeof obj); // Output: object
console.log(typeof arr); // Output: object (arrays are objects in JavaScript)

let variable = "Initial Value";
console.log(variable); // Output: Initial Value

variable = "New Value";
console.log(variable); // Output: New Value

const constantVariable = "Cannot Change";
// Uncommenting the following line will cause an error
// constantVariable = "New Value"; // TypeError: Assignment to constant variable.
console.log(constantVariable); // Output: Cannot Change
